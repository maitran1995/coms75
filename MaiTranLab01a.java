public class MaiTranLab01a {
    public static void main(String[]args) {
        double area ;
        double pi = 3.14 ;
        double radius = 3 ;
        area = radius * radius * pi ;
        System.out.print("area: ");
        System.out.println(area);


        double volume ;
        double length = 10;
        volume = area * length ;
        System.out.print("volume: ");
        System.out.print(volume);


    }
}
